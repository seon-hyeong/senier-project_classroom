package com.seon.project.model;

public class ClassVO {
	private String classId; //수업 아이디
	private String className; //수업 이름
	private String classNum; //수업 번호
	private String classPro;
	private String classDes;
	private String classPoint;
	private String classMajor;
	private String classTime;
	private String classGoal;
	
	
	public String getClassId() {
		return classId;
	}
	public void setClassId(String classId) {
		this.classId = classId;
	}
	public String getClassName() {
		return className;
	}
	public void setClassName(String className) {
		this.className = className;
	}
	public String getClassNum() {
		return classNum;
	}
	public void setClassNum(String classNum) {
		this.classNum = classNum;
	}
	public String getClassPro() {
		return classPro;
	}
	public void setClassPro(String classPro) {
		this.classPro = classPro;
	}
	public String getClassDes() {
		return classDes;
	}
	public void setClassDes(String classDes) {
		this.classDes = classDes;
	}
	public String getClassPoint() {
		return classPoint;
	}
	public void setClassPoint(String classPoint) {
		this.classPoint = classPoint;
	}
	public String getClassMajor() {
		return classMajor;
	}
	public void setClassMajor(String classMajor) {
		this.classMajor = classMajor;
	}
	public String getClassTime() {
		return classTime;
	}
	public void setClassTime(String classTime) {
		this.classTime = classTime;
	}
	public String getClassGoal() {
		return classGoal;
	}
	public void setClassGoal(String classGoal) {
		this.classGoal = classGoal;
	}
	

}
